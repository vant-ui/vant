<template>
  <transition name="image-fade">
    <div class="van-image-preview" ref="previewContainer" v-show="value">
      <van-swipe>
        <van-swipe-item v-for="item in images">
          <img class="van-image-preview__image" @load="handleLoad" :src="item" alt="">
        </van-swipe-item>
      </van-swipe>
    </div>
  </transition>
</template>

<script>
import Vue from 'vue';
import Popup from 'src/mixins/popup';
import VanSwipe from 'packages/swipe';
import VanSwipeItem from 'packages/swipe-item';

export default {
  name: 'van-image-preview',

  mixins: [Popup],

  components: {
    VanSwipe,
    VanSwipeItem
  },

  props: {
    overlay: {
      default: true
    },

    lockOnScroll: {
      default: true
    },

    closeOnClickOverlay: {
      default: true
    }
  },

  data() {
    return {
      images: [],
      viewportSize: null
    };
  },

  methods: {
    handleLoad(event) {
      const container = this.$refs.previewContainer;
      const containerSize = container.getBoundingClientRect();
      const ratio = containerSize.width / containerSize.height;
      const target = event.currentTarget;
      const targetRatio = target.width / target.height;

      const centerClass = 'van-image-preview__image--center';
      const bigClass = 'van-image-preview__image--big';

      if (targetRatio > ratio) {
        target.className += (' ' + centerClass);
      } else {
        target.className += (' ' + bigClass);
      }
    },

    close() {
      /* istanbul ignore if */
      if (this.closing) return;

      this.closing = true;

      this.value = false;

      /* istanbul ignore else */
      if (this.lockOnScroll) {
        setTimeout(() => {
          if (this.overlay && this.bodyOverflow !== 'hidden') {
            document.body.style.overflow = this.bodyOverflow;
          }
          this.bodyOverflow = null;
        }, 200);
      }

      this.opened = false;
      this.doAfterClose();
    }
  },

  mounted() {
    const supportTouch = !Vue.prototype.$isServer && 'ontouchstart' in window;
    const container = this.$refs.previewContainer;

    if (supportTouch) {
      let touchStartTime;

      container.addEventListener('touchstart', () => {
        touchStartTime = new Date();
      });
      container.addEventListener('touchend', () => {
        if (new Date() - touchStartTime < 1500) {
          this.value = false;
        }
      });
    } else {
      container.addEventListener('click', () => {
        this.value = false;
      });
    }
  }
};
</script>
